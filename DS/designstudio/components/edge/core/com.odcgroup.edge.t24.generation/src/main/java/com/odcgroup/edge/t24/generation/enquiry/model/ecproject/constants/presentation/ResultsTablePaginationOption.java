package com.odcgroup.edge.t24.generation.enquiry.model.ecproject.constants.presentation;

import gen.com.acquire.intelligentforms.entities.presentation.html.waihtml.richhtml.RichHTMLPresentationTableWrapper;

import com.acquire.util.AssertionUtils;
import com.odcgroup.edge.t24.generation.enquiry.BasicEnquiryProject;
import com.odcgroup.edge.t24.generation.enquiry.model.Appliable;
import com.odcgroup.edge.t24.generation.enquiry.model.AppliableWithParameter;

/**
 * <code>ResultsTablePaginationOption</code> defines constants for the high-level pagination options that may be applied to an Enquiry results table in the edgeConnect project files
 * generated by {@link BasicEnquiryProject}.<p>
 * 
 * Each such constant "knows" how to apply itself to a supplied {@link RichHTMLPresentationTableWrapper} instance.
 *
 * @author Simon Hayes
 */
public class ResultsTablePaginationOption
{
	public static final AppliableWithParameter<RichHTMLPresentationTableWrapper,Integer> AUTOMATIC_PAGING = new AppliableWithParameter<RichHTMLPresentationTableWrapper,Integer>() {
		@Override
        public void applyTo(RichHTMLPresentationTableWrapper p_presTable, Integer p_numRowsPerPage)
		{
			AssertionUtils.requireNonNull(p_presTable, "p_presTable");
			AssertionUtils.requirePositive(p_numRowsPerPage, "p_numRowsPerPage");
			
			p_presTable.setDisplayStyle("Use automatic paging");
			p_presTable.setRowsPerPage(p_numRowsPerPage);
            p_presTable.setNavigationAbove(Boolean.FALSE);

            p_presTable.setNavigationBackText("< Previous&nbsp;");
            p_presTable.setNavigationForwardText("&nbsp;Next >");
            
            p_presTable.setNavigationStyle(EnquiryStyleStrings.NAVIGATION_ROW_STYLE);
            p_presTable.setNavigationControlStyle(EnquiryStyleStrings.NAVIGATION_CONTROL_STYLE);
		}
	};
	
	public static final Appliable<RichHTMLPresentationTableWrapper> ALL_ROWS_ON_ONE_PAGE = new Appliable<RichHTMLPresentationTableWrapper>() {
		@Override
        public void applyTo(RichHTMLPresentationTableWrapper p_presTable)
		{
			AssertionUtils.requireNonNull(p_presTable, "p_presTable");

			p_presTable.setDisplayStyle("Show all rows on one page");
			p_presTable.setRowsPerPage(null);
		}
	};
}		
