package com.zealcore.se.ui.views;

public class XYDataItem {
    private Number x;
    
    private Number y;
    
    public XYDataItem(final Number x, final Number y) {
        this.x = x;
        this.y = y;
    }

    public Number getX() {
        return x;
    }

    public Number getY() {
        return y;
    }
    
}
